package packone;

public class FirstClass {

	public static void main(String[] args) {
		int a=10; int b=5; int c=a-b;

		// Applying Switch case to verify the value of C variable
		switch(c){
		case 1:
		System.out.println("C value is 1");
		break;
		case 2:
		System.out.println("C Value is 2");
		break;
		case 3:
		System.out.println("C Value is 3");
		break;
		case 4:
		System.out.println("C Value is 4");
		break;
		/*
		 * default: System.out.println("C Value is Out Of Range"); break;
		 */
		}

		
	  
	}

}
